import { z } from "zod";

const envSchema = z.object({
  NODE_ENV: z.enum(["development", "test", "production"]).default("development"),
  PORT: z.coerce.number().int().positive().default(4000),
  FRONTEND_URL: z.url().default("http://localhost:3000"),
  DATABASE_URL: z.string().min(1),
  DIRECT_DATABASE_URL: z.string().min(1).optional(),
  JWT_ACCESS_SECRET: z.string().min(32),
  JWT_REFRESH_SECRET: z.string().min(32),
  API_URL: z.url().optional(),
  REDIS_URL: z.string().min(1).optional(),
  AWS_ACCESS_KEY_ID: z.string().min(1).optional(),
  AWS_SECRET_ACCESS_KEY: z.string().min(1).optional(),
  AWS_REGION: z.string().min(1).optional(),
  AWS_S3_BUCKET: z.string().min(1).optional(),
  SENDGRID_API_KEY: z.string().min(1).optional(),
  SENDGRID_FROM_EMAIL: z.email().optional(),
  SENDGRID_FROM_NAME: z.string().min(1).optional(),
  TERMII_API_KEY: z.string().min(1).optional(),
  TERMII_SENDER_ID: z.string().min(1).optional(),
  PAYSTACK_SECRET_KEY: z.string().min(1).optional(),
  PAYSTACK_PUBLIC_KEY: z.string().min(1).optional(),
  FLUTTERWAVE_SECRET_KEY: z.string().min(1).optional(),
  FLUTTERWAVE_PUBLIC_KEY: z.string().min(1).optional(),
  FLUTTERWAVE_ENCRYPTION_KEY: z.string().min(1).optional(),
  VIRTUAL_ACCOUNT_PROVIDER: z.enum(["providus", "wema"]).optional(),
  PROVIDUS_CLIENT_ID: z.string().min(1).optional(),
  PROVIDUS_SECRET_KEY: z.string().min(1).optional(),
  WEMA_CLIENT_ID: z.string().min(1).optional(),
  WEMA_SECRET_KEY: z.string().min(1).optional(),
  KYC_PROVIDER: z.enum(["youverify", "smile", "prembly"]).optional(),
  YOUVERIFY_API_KEY: z.string().min(1).optional(),
  SMILE_PARTNER_ID: z.string().min(1).optional(),
  SMILE_API_KEY: z.string().min(1).optional(),
  PREMBLY_APP_ID: z.string().min(1).optional(),
  PREMBLY_X_API_KEY: z.string().min(1).optional(),
  TASK_MARKETPLACE_BASE_URL: z.url().optional(),
  TASK_MARKETPLACE_WEBHOOK_SECRET: z.string().min(1).optional(),
  TASK_MARKETPLACE_CLIENT_ID: z.string().min(1).optional(),
  TASK_MARKETPLACE_CLIENT_SECRET: z.string().min(1).optional(),
  FIELD_ENCRYPTION_KEY: z.string().min(1).optional(),
  RATE_LIMIT_WINDOW_MS: z.coerce.number().int().positive().optional(),
  RATE_LIMIT_MAX_REQUESTS: z.coerce.number().int().positive().optional(),
  AUTH_RATE_LIMIT_MAX: z.coerce.number().int().positive().optional()
});

